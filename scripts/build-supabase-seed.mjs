import fs from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const inputPath = path.join(root, "output", "dictionary", "accepted-words.csv");
const outputPath = path.join(root, "supabase", "seeds", "001_dictionary.sql");
const batchSize = 1000;

function quoteSql(value) {
  return `'${String(value).replaceAll("'", "''")}'`;
}

function parseRow(line) {
  const match = line.match(/^([^,]+),(\d+),(accepted),"([^"]*)"$/u);
  if (!match) throw new Error(`Unexpected dictionary CSV row: ${line}`);

  return {
    word: match[1],
    tileLength: Number(match[2]),
    status: match[3],
    sources: match[4].split("|").filter(Boolean),
  };
}

const csv = await fs.readFile(inputPath, "utf8");
const rows = csv
  .split(/\r?\n/u)
  .slice(1)
  .filter(Boolean)
  .map(parseRow);

const statements = [
  "-- Generated by npm run supabase:seed. Do not edit by hand.",
  "begin;",
  "",
];

for (let index = 0; index < rows.length; index += batchSize) {
  const batch = rows.slice(index, index + batchSize);
  statements.push(
    "insert into public.dictionary_words (word, tile_length, status, sources)",
    "values",
    batch
      .map(({ word, tileLength, status, sources }) => {
        const sourceArray = sources.length
          ? `array[${sources.map(quoteSql).join(", ")}]::text[]`
          : "array[]::text[]";
        return `  (${quoteSql(word)}, ${tileLength}, ${quoteSql(status)}, ${sourceArray})`;
      })
      .join(",\n"),
    "on conflict (word) do update set",
    "  tile_length = excluded.tile_length,",
    "  status = excluded.status,",
    "  sources = excluded.sources,",
    "  updated_at = now();",
    "",
  );
}

statements.push("commit;", "");
await fs.mkdir(path.dirname(outputPath), { recursive: true });
await fs.writeFile(outputPath, statements.join("\n"), "utf8");

console.log(`Wrote ${rows.length} accepted words to ${outputPath}`);
